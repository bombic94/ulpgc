    private void convertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertBtnActionPerformed
        
        String exchangeText = exchangeRate.getText();
        String usdText = usdTextField.getText();
        String eurText = eurTextField.getText();
        
        // Check if input is filled in correctly
        if (exchangeText.isEmpty()) {
            errorLabel.setText("You have to fill in the exchange rate");
            return;
        }
        else if (usdText.isEmpty() && eurText.isEmpty()) {
            errorLabel.setText("You have to fill in one of the currencies");
            return;
        }
        else if (!usdText.isEmpty() && !eurText.isEmpty()) {
            errorLabel.setText("You have to fill in only one of the currencies");
            return;
        }
        
        /** Convert EUR to USD and vice versa
        * All numbers are displayed with 2 decimal points
        * Based on the filled currency the program computes value in other currency
        */
        Double exchange = tryParse(exchangeText);
        if (exchange != null) {
            exchangeRate.setText(String.format("%.2f", exchange));
            if (eurText.isEmpty()) {
                Double usd = tryParse(usdText);
                if (usd != null) {
                    Double eur = Math.round(usd / exchange * 100d) / 100d;
                    
                    usdTextField.setText(String.format("%.2f", usd));
                    eurTextField.setText(String.format("%.2f", eur));
                    errorLabel.setText("");
                }    
            } else if (usdText.isEmpty()) {
                Double eur = tryParse(eurText);
                if (eur != null) {
                    Double usd = Math.round(eur * exchange * 100d) / 100d;
                    
                    eurTextField.setText(String.format("%.2f", eur));
                    usdTextField.setText(String.format("%.2f", usd));
                    errorLabel.setText("");
                }    
            }
        }
    }//GEN-LAST:event_convertBtnActionPerformed

