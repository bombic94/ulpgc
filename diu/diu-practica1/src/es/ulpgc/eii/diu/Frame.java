    /**
     * Parses the text to double to two decimals and checks for exception
     * @param text String input
     * @return Parsed double from text
     */
    public Double tryParse(String text) {
        try {
            double d = Double.parseDouble(text);
            return Math.round(d * 100d) / 100d;
        } catch (NumberFormatException e) {
            errorLabel.setText("The input is in wrong format");
            return null;
        }
    }

